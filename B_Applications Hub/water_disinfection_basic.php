                <ul style="margin: 1rem 0; padding-left: 1.5rem;">
                    <li>Drinking water: 95-98% UVT</li>
                    <li>Wastewater: 60-80% UVT</li>
                    <li>Process water: 85-95% UVT</li>
                </ul>
                <p>Pre-filtration may be required for low UVT water.</p>
